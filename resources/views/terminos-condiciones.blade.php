<p class="text-justify"><strong>BENEFICIOS Y USO</strong><br>
Ecocanjes, tiene por objetivo ser un aporte a la sociedad, la protección y
conservación del medio ambiente, y ser una red de comunicación fomentando a los
usuarios la realización de conductas que aporten a dicho fin, por lo cual cada vez
que un usuario aporte al envases para revalorizar recibirá a cambio una cantidad de
ecopesos como recompensa a sus actuaciones, así como podrá canjearlos en en
las plataformas asignada para esto y beneficios por ello. La conversión de productos
depositados por ecopesos está en función del valor que los compradores de estos
productos asignan por kilo; por lo tanto, Ecocanjes utilizará el peso de estos
productos (aplicando una estimación estadística para dicho cálculo, y no pesará los
productos al momento de su depósito para efectos de hacer la distribución de los
kilos que se venderán), para asignar los valores de los ecopesos que recibirán los
usuarios por producto depositado. Es importante saber que los precios de los
productos son variables en el tiempo y dependen de la calidad de los envases
depositados y de los precios internacionales de éstos. Por lo tanto, para saber
cuánto es el valor de los Ecopesos, los usuarios deberán revisar en la página web
de Ecocanjes la información de Saldo en la plataforma de Ecocanjes.<br>
Respecto al canje de productos con ecopesos, la disponibilidad, calidad, cualquier
condición, o falla que presenten los productos, servicios o beneficios ofrecidos por
las empresas asociadas, Ecocanjes se exime de toda responsabilidad; siendo total
y absolutamente responsables de responder al usuario por las garantías o cualquier
perjuicio que se les cause, las mismas empresas que aceptan el canje de ecopesos,
así como las que entregan dichos descuentos o beneficios. Estas empresas
responderán de acuerdo a sus propios términos y políticas de sus respectivos
negocios.<br>
En cuanto a la conversión de productos por ecopesos correspondiente a los
productos reciclados o a la realización de otras conductas ecológicas, se
determinarán exclusivamente por Ecocanjes. Dicha conversión es modificada
unilateralmente cuando se estime conveniente.</p><br>
